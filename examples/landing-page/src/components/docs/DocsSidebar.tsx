function NavItemComponent({ item }: { item: NavItem }) {
  const pathname = usePathname();
  const isActive = pathname === item.href;

  if (!item.href) return null;

  return (
    <Link
      href={item.href}
      className={`docs-nav-link ${isActive ? 'active' : ''}`}
    >
      {item.icon && icons[item.icon] && (
        <span className="docs-nav-icon">{icons[item.icon]}</span>
      )}
      <span>{item.title}</span>
    </Link>
  );
}

function NavSectionComponent({ section }: { section: NavSection }) {
  return (
    <div className="docs-nav-section">
      <h4 className="docs-nav-section-title">{section.title}</h4>
      <div className="docs-nav-section-items">
        {section.items.map((item, index) => (
          <NavItemComponent key={index} item={item} />
        ))}
      </div>
    </div>
  );
}

export function DocsSidebar() {
  return (
    <aside className="docs-sidebar">
      <nav className="docs-nav">
        {docsNav.map((section, index) => (
          <NavSectionComponent key={index} section={section} />
        ))}
      </nav>
    </aside>
  );
}
